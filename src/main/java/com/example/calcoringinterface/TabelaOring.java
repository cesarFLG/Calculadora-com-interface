    public Oring buscarOringMaisProximo(double diametroCalculado, double espessura) {
        List<Oring> tabelaSelecionada;

        switch ((int)(espessura * 100)) {
            case 178:
                tabelaSelecionada = tabela178;
                break;
            case 262:
                tabelaSelecionada = tabela262;
                break;
            case 353:
                tabelaSelecionada = tabela353;
                break;
            default:
                return null;
        }

        Oring maisProximo = null;
        double menorDiferenca = Double.MAX_VALUE;

        for (Oring oring : tabelaSelecionada) {
            double diferenca = Math.abs(oring.diametro - diametroCalculado);
            if (diferenca < menorDiferenca) {
                menorDiferenca = diferenca;
                maisProximo = oring;
            }
        }

        return maisProximo;
    }
